# =========================================================
# الجزء الأول: قراءة البيانات + فهمها + تنظيفها وتجهيزها
# =========================================================

# -----------------------------
# 1) استيراد المكتبات المطلوبة
# -----------------------------
import pandas as pd                  # للتعامل مع الجداول والبيانات
import plotly.express as px          # للرسم البياني التفاعلي
import datetime                      # للتعامل مع التاريخ والوقت عند الحاجة

# -----------------------------------------
# 2) قراءة ملف البيانات من المصدر المباشر
# -----------------------------------------
data_url = "https://cf-courses-data.s3.us.cloud-object-storage.appdomain.cloud/IBM-ML0232EN-SkillsNetwork/asset/18100001.csv"  # رابط ملف CSV
gasoline = pd.read_csv(data_url)     # قراءة البيانات إلى DataFrame اسمه gasoline

# -----------------------------------------
# 3) استعراض أول 5 صفوف من البيانات
# -----------------------------------------
print("أول 5 صفوف من البيانات:")
print(gasoline.head())               # يعرض أول خمس سجلات لفهم شكل البيانات

# -----------------------------------------
# 4) معرفة حجم البيانات
# -----------------------------------------
print("\nأبعاد البيانات:")
print(gasoline.shape)                # يعرض (عدد الصفوف، عدد الأعمدة)

# -----------------------------------------
# 5) فحص أنواع الأعمدة والمعلومات العامة
# -----------------------------------------
print("\nمعلومات عامة عن البيانات:")
gasoline.info()                      # يوضح أسماء الأعمدة وأنواعها وعدد القيم غير الفارغة

# -----------------------------------------
# 6) عرض أسماء الأعمدة
# -----------------------------------------
print("\nأسماء الأعمدة:")
print(gasoline.columns.tolist())     # تحويل أسماء الأعمدة إلى قائمة وطباعتها

# -----------------------------------------
# 7) فحص القيم المفقودة
# -----------------------------------------
print("\nعدد القيم المفقودة في كل عمود:")
print(gasoline.isnull().sum())       # يحسب عدد القيم الفارغة في كل عمود

# =========================================================
# 8) اختيار الأعمدة المهمة فقط وإعادة تسميتها
# =========================================================
# نحتفظ بالأعمدة الأساسية للتحليل لتسهيل العمل وتقليل التشويش
data = gasoline[['REF_DATE', 'GEO', 'Type of fuel', 'VALUE']].rename(
    columns={
        'REF_DATE': 'DATE',          # تغيير اسم العمود ليصبح أوضح
        'Type of fuel': 'TYPE'       # اختصار اسم العمود لسهولة الاستخدام
    }
)

print("\nالبيانات بعد اختيار الأعمدة المهمة:")
print(data.head())                   # مراجعة أول الصفوف بعد التعديل

# =========================================================
# 9) تقسيم عمود GEO إلى City و Province
# =========================================================
# العمود GEO يحتوي عادة على "المدينة، المقاطعة" لذلك نقسمه إلى عمودين مستقلين
data[['City', 'Province']] = data['GEO'].str.split(',', n=1, expand=True)  # split مرة واحدة فقط عند أول فاصلة

# تنظيف المسافات الزائدة من بداية/نهاية النص
data['City'] = data['City'].str.strip()          # إزالة الفراغات من أسماء المدن
data['Province'] = data['Province'].str.strip()  # إزالة الفراغات من أسماء المقاطعات

print("\nالبيانات بعد تقسيم GEO إلى City و Province:")
print(data.head())

# =========================================================
# 10) تحويل التاريخ إلى نوع datetime
# =========================================================
# هذا مهم جدًا حتى نستطيع استخراج السنة والشهر وإجراء فلاتر زمنية
data['DATE'] = pd.to_datetime(data['DATE'], format='%b-%y')   # تحويل النص إلى تاريخ حقيقي
data['Month'] = data['DATE'].dt.month_name().str[:3]          # استخراج أول 3 أحرف من اسم الشهر
data['Year'] = data['DATE'].dt.year                           # استخراج السنة كرقم

print("\nالبيانات بعد تحويل التاريخ واستخراج Month و Year:")
print(data.head())

# =========================================================
# 11) إحصائيات وصفية للمتغير الرقمي
# =========================================================
print("\nإحصائيات وصفية لعمود VALUE:")
print(data['VALUE'].describe())      # يعرض count, mean, std, min, quartiles, max

# =========================================================
# 12) استكشاف القيم الفريدة في الأعمدة الفئوية
# =========================================================
print("\nأنواع الوقود الموجودة في البيانات:")
print(data['TYPE'].unique().tolist())    # عرض جميع فئات الوقود

print("\nبعض المواقع/المدن الموجودة:")
print(data['GEO'].unique().tolist()[:15])  # عرض أول 15 قيمة فقط لتجنب طول المخرجات

print("\nعدد المدن الفريدة:")
print(data['City'].nunique())          # عدد المدن المختلفة

print("\nعدد المقاطعات الفريدة:")
print(data['Province'].nunique())      # عدد المقاطعات المختلفة

# =========================================================
# 13) أمثلة سريعة للاستكشاف الأولي
# =========================================================
print("\nعدد السجلات لكل نوع وقود:")
print(data['TYPE'].value_counts())     # عدّ تكرار كل فئة في عمود TYPE

print("\nعدد السجلات لكل سنة (أول 10 سنوات):")
print(data['Year'].value_counts().sort_index().head(10))  # ترتيب السنوات تصاعديًا

# =========================================================
# 14) حفظ نسخة نظيفة لو أردنا استخدامها لاحقًا
# =========================================================
clean_data = data.copy()               # نسخة آمنة من البيانات بعد التنظيف

print("Part One Done ")